/** Resolve a province name (any of a few spellings) to a JobThai region code. */
export function regionCode(province) {
  const raw = String(province ?? '').trim();
  if (!raw) return '';
  if (REGION_BY_NAME[raw]) return REGION_BY_NAME[raw];
  // tolerate the "กทม."/"กรุงเทพ" shorthand and a stray "จังหวัด" prefix
  const norm = raw.replace(/^จังหวัด/, '').replace(/^กทม\.?$/, 'กรุงเทพมหานคร').trim();
  if (REGION_BY_NAME[norm]) return REGION_BY_NAME[norm];
  const hit = Object.keys(REGION_BY_NAME).find((name) => name.startsWith(norm) || norm.startsWith(name));
  return hit ? REGION_BY_NAME[hit] : '';
}
